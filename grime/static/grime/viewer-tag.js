export function createTagModule(core) {
  var state = core.state;
  var C     = core.C;

  /* ── tag list ──────────────────────────────────────────────── */
  // Rebuild the sidebar tag list from core.TAGS.
  function buildList() {
    var list = document.getElementById('dp-tag-list');
    if (!list) return;
    list.innerHTML = '';
    if (!core.TAGS.length) {
      list.innerHTML = '<div id="dp-tag-empty">No tags yet. Drag to create one.</div>'; return;
    }
    core.TAGS.forEach(function(t) {
      var item = document.createElement('div');
      item.className = 'dp-tag-item'; item.dataset.tagId = t.id;
      var title = document.createElement('div'); title.textContent = t.label; item.appendChild(title);
      if (t.subcomponents && t.subcomponents.length) {
        var sub = document.createElement('div'); sub.className = 'dp-tag-item-sub';
        sub.textContent = t.subcomponents.map(function(s) { return s.label + ': ' + s.text; }).join(' · ');
        item.appendChild(sub);
      }
      item.addEventListener('click', function() { tagOpenExisting(t); });
      list.appendChild(item);
    });
  }

  /* ── tag overlays ──────────────────────────────────────────── */
  // Draw .dp-tag-overlay divs for each tag in draw mode (outside of labeling phase).
  function renderTagOverlays() {
    var viewer = document.getElementById('dp-viewer');
    if (!viewer) return;
    viewer.querySelectorAll('.dp-tag-overlay').forEach(function(el) { el.remove(); });
    var scale = core.getScale();
    core.TAGS.forEach(function(t) {
      var div = document.createElement('div');
      div.className = 'dp-tag-overlay' + (t.autogenerated ? ' autogenerated' : '');
      div.dataset.tagId = t.id;
      div.style.left   = Math.round(t.bbox_left   * scale) + 'px';
      div.style.top    = Math.round(t.bbox_top    * scale) + 'px';
      div.style.width  = Math.round(t.bbox_width  * scale) + 'px';
      div.style.height = Math.round(t.bbox_height * scale) + 'px';
      div.title = t.label + ' (click to edit)';
      var lbl = document.createElement('div'); lbl.className = 'dp-tag-overlay-label'; lbl.textContent = t.label;
      div.appendChild(lbl); viewer.appendChild(div);
    });
  }

  /* ── open existing ─────────────────────────────────────────── */
  // Load an existing tag into pending state and enter labeling mode.
  function tagOpenExisting(tag) {
    state.tagEditingId      = tag.id;
    state.tagPendingLabel   = tag.label;
    state.tagPendingSubcomps = tag.subcomponents ? tag.subcomponents.slice() : [];
    state.tagPendingBbox    = { left: tag.bbox_left, top: tag.bbox_top, width: tag.bbox_width, height: tag.bbox_height };
    tagEnterLabelingMode();
    document.getElementById('dp-tag-delete-btn').style.display = '';
  }

  /* ── labeling mode ─────────────────────────────────────────── */
  // Switch the tag panel to labeling mode, optionally pre-filling NER suggestions.
  function tagEnterLabelingMode() {
    state.tagPhase = 'labeling';
    state.tagSubSelectedIds.clear();
    if (!state.tagEditingId && state.tagPendingBbox && core.nerSuggestSubcomps) {
      core.nerSuggestSubcomps(state.tagPendingBbox);
      tagUpdateSubcompDisplay();
    }
    document.getElementById('dp-tag-list-view').style.display = 'none';
    document.getElementById('dp-tag-label-view').style.display = 'flex';
    document.getElementById('dp-tag-label-heading').textContent = state.tagEditingId ? 'Edit tag' : 'New tag';
    var labelInput = document.getElementById('dp-tag-label-input');
    labelInput.value = state.tagPendingLabel;
    document.getElementById('dp-tag-delete-btn').style.display = 'none';
    document.getElementById('dp-tag-sub-input-row').style.display = 'none';
    tagUpdateSubcompDisplay();
    updateVeils();
    core.renderOverlays();
    labelInput.focus(); labelInput.select();
  }

  // Reset all pending tag state and return to draw mode.
  function tagExitLabelingMode() {
    state.tagPhase = 'draw'; state.tagEditingId = null; state.tagPendingLabel = '';
    state.tagPendingSubcomps = []; state.tagPendingBbox = null;
    state.tagSubSelectedIds.clear(); state.tagSubDrawStart = null;
    state.isDragging = false; state.tagResizing = null;
    document.getElementById('dp-tag-label-view').style.display = 'none';
    document.getElementById('dp-tag-list-view').style.display = '';
    document.getElementById('dp-tag-sub-input-row').style.display = 'none';
    document.getElementById('dp-tag-active-rect').style.display = 'none';
    document.getElementById('dp-tag-sub-rect').style.display = 'none';
    ['dp-veil-top','dp-veil-bottom','dp-veil-left','dp-veil-right'].forEach(function(id) {
      document.getElementById(id).style.display = 'none';
    });
    buildList(); core.renderOverlays(); core.updateBadge();
  }

  /* ── veils ─────────────────────────────────────────────────── */
  // Reposition the four veil overlays and the active-rect border around tagPendingBbox.
  function updateVeils() {
    if (!state.tagPendingBbox) return;
    var viewer = document.getElementById('dp-viewer'), scale = core.getScale();
    var bx = Math.round(state.tagPendingBbox.left   * scale);
    var by = Math.round(state.tagPendingBbox.top    * scale);
    var bw = Math.round(state.tagPendingBbox.width  * scale);
    var bh = Math.round(state.tagPendingBbox.height * scale);
    var vw = viewer.offsetWidth, vh = viewer.offsetHeight;
    function setVeil(id, l, t, w, h) {
      var el = document.getElementById(id);
      el.style.left = l+'px'; el.style.top = t+'px'; el.style.width = w+'px'; el.style.height = h+'px';
      el.style.display = (w > 0 && h > 0) ? '' : 'none';
    }
    setVeil('dp-veil-top',    0,     0,     vw,    by);
    setVeil('dp-veil-bottom', 0,     by+bh, vw,    Math.max(0, vh - by - bh));
    setVeil('dp-veil-left',   0,     by,    bx,    bh);
    setVeil('dp-veil-right',  bx+bw, by,    Math.max(0, vw - bx - bw), bh);
    var ar = document.getElementById('dp-tag-active-rect');
    ar.style.left = bx+'px'; ar.style.top = by+'px'; ar.style.width = bw+'px'; ar.style.height = bh+'px';
    ar.style.display = 'block';
  }

  /* ── subcomp display ───────────────────────────────────────── */
  // Rebuild the subcomponent list in the panel from state.tagPendingSubcomps.
  function tagUpdateSubcompDisplay() {
    var list = document.getElementById('dp-tag-subcomp-list');
    list.innerHTML = '';
    var firstSelected = null;
    state.tagPendingSubcomps.forEach(function(sc, i) {
      var isSelected = state.tagSubSelectedIds.has(sc.word_id);
      var row = document.createElement('div');
      row.className = 'dp-subcomp-row' + (isSelected ? ' highlighted' : '');
      var lbl = document.createElement('span'); lbl.className = 'dp-subcomp-label'; lbl.textContent = sc.label + ':';
      var txt = document.createElement('span'); txt.className = 'dp-subcomp-text';  txt.textContent = sc.text;
      var rm  = document.createElement('span'); rm.className  = 'dp-subcomp-rm';    rm.textContent  = '×'; rm.title = 'Remove from pattern';
      (function(idx) { rm.addEventListener('click', function(e) {
        e.stopPropagation();
        state.tagSubSelectedIds.delete(state.tagPendingSubcomps[idx].word_id);
        state.tagPendingSubcomps.splice(idx, 1);
        tagMaybeShowSubInput(); tagUpdateSubcompDisplay(); core.renderOverlays();
      }); })(i);
      (function(wid) { row.addEventListener('click', function(e) {
        if (e.target.classList.contains('dp-subcomp-rm')) return;
        if (state.tagSubSelectedIds.has(wid)) state.tagSubSelectedIds.delete(wid); else state.tagSubSelectedIds.add(wid);
        tagMaybeShowSubInput(); tagUpdateSubcompDisplay(); core.renderOverlays();
      }); })(sc.word_id);
      row.appendChild(lbl); row.appendChild(txt); row.appendChild(rm);
      list.appendChild(row);
      if (isSelected && !firstSelected) firstSelected = row;
    });
    if (firstSelected) firstSelected.scrollIntoView({ block: 'nearest', behavior: 'smooth' });
    core.renderOverlays();
  }

  /* ── ditto helpers ─────────────────────────────────────────── */
  // Return true if the text consists entirely of ditto-mark characters.
  function isDitto(text) {
    var t = text.trim();
    return /^["'«»''""″〃〞＂]+$/.test(t) || t === '""' || t === "''" || t === '``';
  }

  // Find the nearest preceding tag with the given subcomponent label and return its non-ditto text.
  function resolveDitto(label, currentTop) {
    var candidates = core.TAGS.filter(function(t) { return t.bbox_top + t.bbox_height <= currentTop + 8; });
    candidates.sort(function(a, b) { return b.bbox_top - a.bbox_top; });
    for (var i = 0; i < candidates.length; i++) {
      var sc = (candidates[i].subcomponents || []).find(function(s) { return s.label === label; });
      if (sc && sc.text && !isDitto(sc.text)) return sc.text;
    }
    return null;
  }

  /* ── sub-label ─────────────────────────────────────────────── */
  // Assign label to all currently selected sub-words, resolving dittos where needed.
  function tagConfirmSubLabel(label) {
    label = label.trim();
    if (!label || !state.tagSubSelectedIds.size) return;
    state.tagSubSelectedIds.forEach(function(wid) {
      var w = core.wordById[wid]; if (!w) return;
      var existing = state.tagPendingSubcomps.find(function(s) { return s.word_id === wid; });
      if (existing) { existing.label = label; }
      else {
        var text = w.text || '';
        if (isDitto(text) && state.tagPendingBbox) { var resolved = resolveDitto(label, state.tagPendingBbox.top); if (resolved) text = resolved; }
        state.tagPendingSubcomps.push({ word_id: wid, label: label, text: text });
      }
    });
    state.tagSubSelectedIds.clear();
    document.getElementById('dp-tag-sub-input').value = '';
    document.getElementById('dp-tag-sub-input-row').style.display = 'none';
    tagUpdateSubcompDisplay(); populateLabelDatalist();
  }

  // Return the shared label of all selected sub-words, or '' if they differ.
  function _commonSelectedLabel() {
    var seen = null, all = true;
    state.tagSubSelectedIds.forEach(function(wid) {
      var sc = state.tagPendingSubcomps.find(function(s) { return s.word_id === wid; });
      if (!sc) { all = false; return; }
      if (seen === null) seen = sc.label; else if (seen !== sc.label) all = false;
    });
    return (all && seen !== null) ? seen : '';
  }

  // Show the sub-label input row when words are selected, hide it when the selection is empty.
  function tagMaybeShowSubInput() {
    var row = document.getElementById('dp-tag-sub-input-row');
    if (state.tagSubSelectedIds.size > 0) {
      document.getElementById('dp-tag-sub-input').value = _commonSelectedLabel();
      row.style.display = '';
    } else { document.getElementById('dp-tag-sub-input').value = ''; row.style.display = 'none'; }
    core.renderOverlays();
  }

  /* ── sub-word selection ────────────────────────────────────── */
  // Toggle selection of a single sub-word by id, ignoring clicks outside the pending bbox.
  function tagSubWordClick(wordId, shiftKey) {
    if (!state.tagPendingBbox) return;
    var w = core.wordById[wordId]; if (!w) return;
    if (w.left < state.tagPendingBbox.left || w.top < state.tagPendingBbox.top ||
        w.left + w.width > state.tagPendingBbox.left + state.tagPendingBbox.width + 4 ||
        w.top + w.height > state.tagPendingBbox.top  + state.tagPendingBbox.height + 4) return;
    if (!shiftKey) state.tagSubSelectedIds.clear();
    if (state.tagSubSelectedIds.has(wordId)) state.tagSubSelectedIds.delete(wordId); else state.tagSubSelectedIds.add(wordId);
    tagMaybeShowSubInput(); tagUpdateSubcompDisplay();
  }

  // Select sub-words inside the given viewer-local rect that also lie within the pending bbox.
  function tagSubSelectInRect(x1, y1, x2, y2, shiftKey) {
    if (!state.tagPendingBbox) return;
    if (!shiftKey) state.tagSubSelectedIds.clear();
    var scale = core.getScale(), px1 = x1/scale, py1 = y1/scale, px2 = x2/scale, py2 = y2/scale;
    var rb = state.tagPendingBbox;
    core.OCR_WORDS.forEach(function(w) {
      if ((w.left+w.width) > px1 && w.left < px2 && (w.top+w.height) > py1 && w.top < py2 &&
          w.left >= rb.left - 4 && w.top >= rb.top - 4 &&
          w.left + w.width <= rb.left + rb.width + 4 && w.top + w.height <= rb.top + rb.height + 4)
        state.tagSubSelectedIds.add(w.id);
    });
    tagMaybeShowSubInput(); tagUpdateSubcompDisplay();
  }

  /* ── datalist ──────────────────────────────────────────────── */
  // Populate the sub-label autocomplete datalist from all existing tags and pending subcomps.
  function populateLabelDatalist() {
    var dl = document.getElementById('dp-sub-label-list'); if (!dl) return;
    var seen = {};
    core.TAGS.forEach(function(t) { (t.subcomponents || []).forEach(function(sc) { if (sc.label) seen[sc.label] = true; }); });
    state.tagPendingSubcomps.forEach(function(sc) { if (sc.label) seen[sc.label] = true; });
    dl.innerHTML = '';
    Object.keys(seen).sort().forEach(function(lbl) { var opt = document.createElement('option'); opt.value = lbl; dl.appendChild(opt); });
  }

  /* ── save / delete ─────────────────────────────────────────── */
  // POST create or update for the pending tag and exit labeling mode on success.
  function tagSave() {
    var label = document.getElementById('dp-tag-label-input').value.trim();
    if (!label) { document.getElementById('dp-tag-label-input').focus(); return; }
    if (!state.tagPendingBbox) return;
    var subcomps = state.tagPendingSubcomps.slice();
    var btn = document.getElementById('dp-tag-save-btn'); btn.disabled = true; btn.textContent = 'Saving…';
    var url, body;
    if (state.tagEditingId) {
      url = C.tagUpdateUrl;
      body = 'tag_id=' + state.tagEditingId + '&label=' + encodeURIComponent(label) +
             '&bbox_left=' + state.tagPendingBbox.left + '&bbox_top=' + state.tagPendingBbox.top +
             '&bbox_width=' + state.tagPendingBbox.width + '&bbox_height=' + state.tagPendingBbox.height +
             '&subcomponents=' + encodeURIComponent(JSON.stringify(subcomps));
    } else {
      url = C.tagCreateUrl;
      body = 'source_type_id=' + C.tagSourceTypeId + '&source_id=' + C.tagSourceId +
             '&label=' + encodeURIComponent(label) +
             '&bbox_left=' + state.tagPendingBbox.left + '&bbox_top=' + state.tagPendingBbox.top +
             '&bbox_width=' + state.tagPendingBbox.width + '&bbox_height=' + state.tagPendingBbox.height +
             '&subcomponents=' + encodeURIComponent(JSON.stringify(subcomps));
    }
    core.postJson(url, body).then(function(data) {
      btn.disabled = false; btn.textContent = 'Save ↵';
      if (!data.ok) { alert(data.error || 'Error saving tag.'); return; }
      if (state.tagEditingId) {
        var idx = core.TAGS.findIndex(function(t) { return t.id === state.tagEditingId; });
        if (idx >= 0) core.TAGS[idx] = data.tag;
      } else { core.TAGS.push(data.tag); adjustDocTagCount(1); }
      populateLabelDatalist(); tagExitLabelingMode();
    }).catch(function() { btn.disabled = false; btn.textContent = 'Save ↵'; });
  }

  // POST delete for the current tag and remove it from core.TAGS.
  function tagDelete() {
    if (!state.tagEditingId) return;
    if (!confirm('Delete this tag?')) return;
    core.postJson(C.tagDeleteUrl, 'tag_id=' + state.tagEditingId).then(function(data) {
      if (!data.ok) { alert('Error deleting tag.'); return; }
      var id = state.tagEditingId;
      core.TAGS = core.TAGS.filter(function(t) { return t.id !== id; });
      adjustDocTagCount(-1); tagExitLabelingMode();
    });
  }

  // Increment or decrement the document-level tag count badge.
  function adjustDocTagCount(delta) {
    var el = document.getElementById('dp-doc-tag-count-val'); if (!el) return;
    var n = (parseInt(el.textContent, 10) || 0) + delta; el.textContent = n;
    var suffix = document.getElementById('dp-doc-tag-count-suffix');
    if (suffix) suffix.textContent = 'tag' + (n === 1 ? '' : 's');
  }

  /* ── render ────────────────────────────────────────────────── */
  // In labeling phase: render ghost word overlays inside the bbox; otherwise render tag overlays.
  function render(viewer, scale) {
    if (state.tagPhase === 'labeling') {
      var taggedIds = new Set(state.tagPendingSubcomps.map(function(s) { return s.word_id; }));
      core.OCR_WORDS.forEach(function(w) {
        if (state.tagPendingBbox) {
          var rb = state.tagPendingBbox;
          if (w.left < rb.left - 4 || w.top < rb.top - 4 ||
              w.left + w.width  > rb.left + rb.width  + 4 ||
              w.top  + w.height > rb.top  + rb.height + 4) return;
        }
        var div = document.createElement('div');
        var cls = 'dp-word-ghost';
        if (state.tagSubSelectedIds.has(w.id)) cls += ' sub-selected';
        else if (taggedIds.has(w.id)) cls += ' sub-tagged';
        div.className    = cls; div.dataset.wordId = w.id;
        div.style.left   = Math.round(w.left   * scale) + 'px';
        div.style.top    = Math.round(w.top    * scale) + 'px';
        div.style.width  = Math.round(w.width  * scale) + 'px';
        div.style.height = Math.round(w.height * scale) + 'px';
        div.title = w.text;
        viewer.appendChild(div);
      });
    } else {
      renderTagOverlays();
    }
  }

  /* ── mouse handlers ────────────────────────────────────────── */
  // Begin a tag draw gesture or a sub-word selection gesture.
  function onMousedown(e) {
    if (state.tagPhase === 'draw') {
      e.preventDefault(); state.tagDrawStart = { x: e.clientX, y: e.clientY }; state.isDragging = false;
    } else if (state.tagPhase === 'labeling') {
      var ghostEl = e.target.closest ? e.target.closest('.dp-word-ghost') : null;
      if (!ghostEl) { e.preventDefault(); state.tagSubDrawStart = { x: e.clientX, y: e.clientY }; state.isDragging = false; }
    }
  }

  // Handle corner-resize drag, tag draw rubber-band, or sub-word selection rubber-band.
  function onMousemove(e) {
    if (state.tagResizing) {
      var scale = core.getScale();
      var dx = (e.clientX - state.tagResizing.startX) / scale;
      var dy = (e.clientY - state.tagResizing.startY) / scale;
      var b  = state.tagResizing.startBbox;
      var nb = { left: b.left, top: b.top, width: b.width, height: b.height };
      var MIN = 10;
      switch (state.tagResizing.corner) {
        case 'tl': nb.left = Math.round(b.left+dx); nb.top = Math.round(b.top+dy); nb.width = Math.max(MIN, Math.round(b.width-dx)); nb.height = Math.max(MIN, Math.round(b.height-dy)); if (nb.width===MIN) nb.left=b.left+b.width-MIN; if (nb.height===MIN) nb.top=b.top+b.height-MIN; break;
        case 'tr': nb.top = Math.round(b.top+dy); nb.width = Math.max(MIN, Math.round(b.width+dx)); nb.height = Math.max(MIN, Math.round(b.height-dy)); if (nb.height===MIN) nb.top=b.top+b.height-MIN; break;
        case 'bl': nb.left = Math.round(b.left+dx); nb.width = Math.max(MIN, Math.round(b.width-dx)); nb.height = Math.max(MIN, Math.round(b.height+dy)); if (nb.width===MIN) nb.left=b.left+b.width-MIN; break;
        case 'br': nb.width = Math.max(MIN, Math.round(b.width+dx)); nb.height = Math.max(MIN, Math.round(b.height+dy)); break;
      }
      state.tagPendingBbox = nb; updateVeils(); core.renderOverlays(); return;
    }
    if (state.tagPhase === 'draw' && state.tagDrawStart) {
      var dx2 = e.clientX - state.tagDrawStart.x, dy2 = e.clientY - state.tagDrawStart.y;
      if (!state.isDragging && Math.sqrt(dx2*dx2 + dy2*dy2) > 5) state.isDragging = true;
      if (state.isDragging) {
        var vr = document.getElementById('dp-viewer').getBoundingClientRect();
        var dr = document.getElementById('dp-tag-draw-rect');
        core.updateRubberBand(dr, state.tagDrawStart.x, state.tagDrawStart.y, e.clientX, e.clientY, vr);
      }
    } else if (state.tagPhase === 'labeling' && state.tagSubDrawStart) {
      var dx3 = e.clientX - state.tagSubDrawStart.x, dy3 = e.clientY - state.tagSubDrawStart.y;
      if (!state.isDragging && Math.sqrt(dx3*dx3 + dy3*dy3) > 5) state.isDragging = true;
      if (state.isDragging) {
        var vr2 = document.getElementById('dp-viewer').getBoundingClientRect();
        var sr  = document.getElementById('dp-tag-sub-rect');
        core.updateRubberBand(sr, state.tagSubDrawStart.x, state.tagSubDrawStart.y, e.clientX, e.clientY, vr2);
      }
    }
  }

  // Finalise a draw, resize, or sub-selection gesture; open an existing tag on single click.
  function onMouseup(e) {
    if (state.tagResizing) { state.tagResizing = null; return; }
    if (state.tagPhase === 'draw') {
      document.getElementById('dp-tag-draw-rect').style.display = 'none';
      if (state.isDragging && state.tagDrawStart) {
        var vr = document.getElementById('dp-viewer').getBoundingClientRect(), scale = core.getScale();
        var px = Math.min(state.tagDrawStart.x, e.clientX) - vr.left;
        var py = Math.min(state.tagDrawStart.y, e.clientY) - vr.top;
        var pw = Math.abs(e.clientX - state.tagDrawStart.x), ph = Math.abs(e.clientY - state.tagDrawStart.y);
        pw = Math.max(0, pw - Math.max(0, -px)); ph = Math.max(0, ph - Math.max(0, -py));
        px = Math.max(0, px); py = Math.max(0, py);
        if (pw > 4 && ph > 4) {
          state.tagPendingBbox = { left: Math.round(px/scale), top: Math.round(py/scale), width: Math.round(pw/scale), height: Math.round(ph/scale) };
          state.tagPendingLabel = ''; state.tagPendingSubcomps = []; state.tagEditingId = null;
          tagEnterLabelingMode();
        }
      } else if (!state.isDragging) {
        var nerEl = e.target.closest ? e.target.closest('.dp-ner-overlay') : null;
        if (nerEl && nerEl.dataset.wordIds) {
          if (core.nerOpenPopup) core.nerOpenPopup(nerEl.dataset.wordIds.split(',').map(Number));
        } else {
          var vrc = document.getElementById('dp-viewer').getBoundingClientRect(), sc = core.getScale();
          var cpx = (e.clientX - vrc.left) / sc, cpy = (e.clientY - vrc.top) / sc;
          var hit = core.TAGS.find(function(t) { return cpx >= t.bbox_left && cpy >= t.bbox_top && cpx <= t.bbox_left+t.bbox_width && cpy <= t.bbox_top+t.bbox_height; });
          if (hit) tagOpenExisting(hit);
        }
      }
      state.tagDrawStart = null; state.isDragging = false;
    } else if (state.tagPhase === 'labeling') {
      document.getElementById('dp-tag-sub-rect').style.display = 'none';
      if (state.isDragging && state.tagSubDrawStart) {
        var vr4 = document.getElementById('dp-viewer').getBoundingClientRect();
        tagSubSelectInRect(
          Math.min(state.tagSubDrawStart.x, e.clientX) - vr4.left, Math.min(state.tagSubDrawStart.y, e.clientY) - vr4.top,
          Math.max(state.tagSubDrawStart.x, e.clientX) - vr4.left, Math.max(state.tagSubDrawStart.y, e.clientY) - vr4.top,
          e.shiftKey
        );
      } else if (!state.isDragging) {
        var ghostEl2 = e.target.closest ? e.target.closest('.dp-word-ghost') : null;
        if (ghostEl2 && ghostEl2.dataset.wordId) tagSubWordClick(parseInt(ghostEl2.dataset.wordId), e.shiftKey);
      }
      state.tagSubDrawStart = null; state.isDragging = false;
    }
    e.stopPropagation();
  }

  /* ── keyboard ──────────────────────────────────────────────── */
  // Handle Enter (confirm sub-label or save), Escape (deselect or exit), Delete (remove subcomps).
  function onKeydown(e) {
    if (state.tagPhase !== 'labeling') return;
    var subInput   = document.getElementById('dp-tag-sub-input');
    var labelInput = document.getElementById('dp-tag-label-input');
    var subActive  = subInput && subInput.parentElement.style.display !== 'none' && document.activeElement === subInput;
    if (e.key === 'Enter') {
      if (subActive) { e.preventDefault(); tagConfirmSubLabel(subInput.value); return; }
      if (document.activeElement === labelInput) { e.preventDefault(); labelInput.blur(); return; }
      e.preventDefault(); tagSave(); return;
    }
    if (e.key === 'Escape') {
      if (subActive) {
        e.preventDefault(); state.tagSubSelectedIds.clear();
        document.getElementById('dp-tag-sub-input-row').style.display = 'none'; core.renderOverlays(); return;
      }
      e.preventDefault(); tagExitLabelingMode(); return;
    }
    if ((e.key === 'Delete' || e.key === 'Backspace') && !subActive && document.activeElement !== labelInput && state.tagSubSelectedIds.size > 0) {
      e.preventDefault();
      state.tagSubSelectedIds.forEach(function(wid) {
        var idx = state.tagPendingSubcomps.findIndex(function(s) { return s.word_id === wid; });
        if (idx >= 0) state.tagPendingSubcomps.splice(idx, 1);
      });
      state.tagSubSelectedIds.clear();
      document.getElementById('dp-tag-sub-input-row').style.display = 'none';
      tagUpdateSubcompDisplay(); core.renderOverlays(); return;
    }
  }

  // Exit labeling mode (used by core when switching away from the label tab).
  function clearSelection() {
    if (state.tagPhase === 'labeling') tagExitLabelingMode();
  }

  /* ── setup UI ──────────────────────────────────────────────── */
  // Attach tag panel button listeners and corner-resize handle listeners.
  function setupUI() {
    document.getElementById('dp-tag-save-btn').addEventListener('click', function(e) { e.preventDefault(); tagSave(); });
    document.getElementById('dp-tag-cancel-btn').addEventListener('click', function(e) { e.preventDefault(); tagExitLabelingMode(); });
    document.getElementById('dp-tag-delete-btn').addEventListener('click', function(e) { e.preventDefault(); tagDelete(); });
    document.getElementById('dp-tag-sub-clear-btn').addEventListener('click', function(e) {
      e.preventDefault();
      state.tagSubSelectedIds.forEach(function(wid) {
        var idx = state.tagPendingSubcomps.findIndex(function(s) { return s.word_id === wid; });
        if (idx >= 0) state.tagPendingSubcomps.splice(idx, 1);
      });
      state.tagSubSelectedIds.clear();
      document.getElementById('dp-tag-sub-input').value = '';
      document.getElementById('dp-tag-sub-input-row').style.display = 'none';
      tagUpdateSubcompDisplay(); core.renderOverlays();
    });

    document.querySelectorAll('.dp-tag-corner').forEach(function(handle) {
      handle.addEventListener('mousedown', function(e) {
        if (state.tagPhase !== 'labeling' || !state.tagPendingBbox) return;
        e.preventDefault(); e.stopPropagation();
        state.tagResizing = { corner: handle.dataset.corner, startX: e.clientX, startY: e.clientY, startBbox: Object.assign({}, state.tagPendingBbox) };
      });
    });
  }

  return {
    render, onMousedown, onMousemove, onMouseup, onKeydown, setupUI,
    clearSelection, updateVeils,
    buildList, renderTagOverlays, populateLabelDatalist,
  };
}
